// ── Helper functions ──────────────────────────────────────────────────────────

export function getMessageById(id: string): Message | undefined {
  return messages.find((m) => m.id === id)
}

export function getMessagesByStatus(
  status: Message["status"],
): Message[] {
  return messages.filter((m) => m.status === status)
}

export function getMessagesByChannel(channel: MessageChannel): Message[] {
  return messages.filter((m) => m.channel === channel)
}

export function searchMessages(query: string): Message[] {
  const q = query.toLowerCase()
  return messages.filter(
    (m) =>
      m.subject.toLowerCase().includes(q) ||
      m.body.toLowerCase().includes(q) ||
      m.audience.toLowerCase().includes(q),
  )
}

export function getRecentMessages(limit = 10): Message[] {
  return [...messages]
    .sort(
      (a, b) =>
        new Date(b.sentAt).getTime() - new Date(a.sentAt).getTime(),
    )
    .slice(0, limit)
}
