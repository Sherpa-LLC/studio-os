// ── Helper functions ──────────────────────────────────────────────────────────

export function getHouseholdById(id: string): Household | undefined {
  return households.find((h) => h.id === id)
}

export function searchHouseholds(query: string): Household[] {
  const q = query.toLowerCase()
  return households.filter((h) =>
    h.guardians.some(
      (g) =>
        g.firstName.toLowerCase().includes(q) ||
        g.lastName.toLowerCase().includes(q) ||
        g.email.toLowerCase().includes(q),
    ),
  )
}

export function getHouseholdsByStatus(
  status: Household["status"],
): Household[] {
  return households.filter((h) => h.status === status)
}

export function getHouseholdsWithBalance(): Household[] {
  return households.filter((h) => h.balance > 0)
}
