export function getTeamById(id: string): CompetitionTeam | undefined {
  return teams.find((t) => t.id === id)
}

export function getTeamRoster(teamId: string): CompetitionTeam["roster"] {
  const team = getTeamById(teamId)
  return team?.roster ?? []
}
