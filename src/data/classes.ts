// ── Billing helpers ──────────────────────────────────────────────────────────

function parseTime(t: string): number {
  const [h, m] = t.split(":").map(Number)
  return h * 60 + m
}

export function getClassDurationHours(cls: Class): number {
  const mins = parseTime(cls.schedule.endTime) - parseTime(cls.schedule.startTime)
  return mins / 60
}

export function getClassMonthlyTuition(cls: Class): number {
  return +(getClassDurationHours(cls) * billingConfig.hourlyRate).toFixed(2)
}

// ── Helper functions ──────────────────────────────────────────────────────────

export function getClassById(id: string): Class | undefined {
  return classes.find((c) => c.id === id)
}

export function getClassesByDay(day: DayOfWeek): Class[] {
  return classes.filter((c) => c.schedule.day === day)
}

export function getClassesByDiscipline(discipline: Discipline): Class[] {
  return classes.filter((c) => c.discipline === discipline)
}

export function getClassesByAgeGroup(ageGroup: AgeGroup): Class[] {
  return classes.filter((c) => c.ageGroup === ageGroup)
}

export function getClassesByInstructor(instructorId: string): Class[] {
  return classes.filter((c) => c.instructorId === instructorId)
}

export function getClassesByRoom(room: Class["schedule"]["room"]): Class[] {
  return classes.filter((c) => c.schedule.room === room)
}

export function searchClasses(query: string): Class[] {
  const q = query.toLowerCase()
  return classes.filter(
    (c) =>
      c.name.toLowerCase().includes(q) ||
      c.discipline.toLowerCase().includes(q) ||
      c.description.toLowerCase().includes(q),
  )
}

export function getActiveClasses(): Class[] {
  return classes.filter((c) => c.status === "active")
}
