export function getInstructorById(id: string): Instructor | undefined {
  return instructors.find((i) => i.id === id)
}

export function getInstructorsByDiscipline(
  discipline: Discipline,
): Instructor[] {
  return instructors.filter((i) => i.specialties.includes(discipline))
}

export function getInstructorName(id: string): string {
  const inst = getInstructorById(id)
  return inst ? `${inst.firstName} ${inst.lastName}` : "Unknown"
}
